package com.metalsa.model;

public class ParameterConfig {
	private int sequence;
	private String headerSw;
	private String typeId;
	private String displayName;;
	private String masterSw;
	private String activeSw;
	private int parentId;
	private String parameterType;
	
	public int getSequence() {
		return sequence;
	}
	public void setSequence(int sequence) {
		this.sequence = sequence;
	}
	public String getHeaderSw() {
		return headerSw;
	}
	public void setHeaderSw(String headerSw) {
		this.headerSw = headerSw;
	}
	public String getTypeId() {
		return typeId;
	}
	public void setTypeId(String typeId) {
		this.typeId = typeId;
	}
	public String getDisplayName() {
		return displayName;
	}
	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}
	public String getMasterSw() {
		return masterSw;
	}
	public void setMasterSw(String masterSw) {
		this.masterSw = masterSw;
	}
	public String getActiveSw() {
		return activeSw;
	}
	public void setActiveSw(String activeSw) {
		this.activeSw = activeSw;
	}
	public int getParentId() {
		return parentId;
	}
	public void setParentId(int parentId) {
		this.parentId = parentId;
	}
	public String getParameterType() {
		return parameterType;
	}
	public void setParameterType(String parameterType) {
		this.parameterType = parameterType;
	}
	
	
}
