package com.metalsa.model;

public class DataSheetDashboardModel {
	
	private String datasheetId, datasheetName, addedBy, createdOn, approvedBy, approvedOn, userName;

	public String getDatasheetId() {
		return datasheetId;
	}

	public void setDatasheetId(String datasheetId) {
		this.datasheetId = datasheetId;
	}

	public String getDatasheetName() {
		return datasheetName;
	}

	public void setDatasheetName(String datasheetName) {
		this.datasheetName = datasheetName;
	}

	public String getAddedBy() {
		return addedBy;
	}

	public void setAddedBy(String addedBy) {
		this.addedBy = addedBy;
	}

	public String getCreatedOn() {
		return createdOn;
	}

	public void setCreatedOn(String createdOn) {
		this.createdOn = createdOn;
	}

	public String getApprovedBy() {
		return approvedBy;
	}

	public void setApprovedBy(String approvedBy) {
		this.approvedBy = approvedBy;
	}

	public String getApprovedOn() {
		return approvedOn;
	}

	public void setApprovedOn(String approvedOn) {
		this.approvedOn = approvedOn;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}
}
